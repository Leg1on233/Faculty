    function initSlider() {
        if ($('.hero-slider').length === 0) return;
        let currentSlide = 1;
        const totalSlides = 3;
        let autoPlayInterval;

        function showSlide(n) {
            const slides = $('.slide');
            const dots = $('.dot');
            if (n > totalSlides) currentSlide = 1;
            if (n < 1) currentSlide = totalSlides;
            slides.removeClass('active');
            dots.removeClass('active');
            slides.eq(currentSlide - 1).addClass('active');
            dots.eq(currentSlide - 1).addClass('active');
        }

        function nextSlide() { currentSlide++; showSlide(currentSlide); }
        function prevSlide() { currentSlide--; showSlide(currentSlide); }

        function autoPlay() {
            autoPlayInterval = setInterval(() => { nextSlide(); }, 15000);
        }

        function stopAutoPlay() { if (autoPlayInterval) clearInterval(autoPlayInterval); }

        $('.slider-next').click(() => { stopAutoPlay(); nextSlide(); autoPlay(); });
        $('.slider-prev').click(() => { stopAutoPlay(); prevSlide(); autoPlay(); });
        $('.dot').click(function() {
            stopAutoPlay();
            currentSlide = parseInt($(this).data('slide'));
            showSlide(currentSlide);
            autoPlay();
        });

        $('.slide').hover(() => stopAutoPlay(), () => autoPlay());
        autoPlay();
    }

